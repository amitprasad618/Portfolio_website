// JavaScript to open and close the dialog box
const openDialogButton1 = document.getElementById('open-dialog-button1');
const dialogOverlay1 = document.getElementById('dialog-overlay1');
const closeDialogButton1 = document.getElementById('close-dialog-button1');

openDialogButton1.addEventListener('click', () => {
    dialogOverlay1.style.display = 'block';
    closeDialogButton1.style.display = 'block'; // Show the close button
});

closeDialogButton1.addEventListener('click', () => {
    dialogOverlay1.style.display = 'none';
    closeDialogButton1.style.display = 'none'; // Hide the close button
});


